void PrintHexFrame(const std::vector<uint8_t> &frame)
{
    int i = 1;
    for (const auto &byte : frame)
    {
        std::cout << std::setw(2) << std::setfill('0') << std::hex << static_cast<int>(byte) << " ";
        if (i % 10 == 0)
            std::cout << "\n";
        i++;
    }
    std::cout << std::dec << std::endl;
}


